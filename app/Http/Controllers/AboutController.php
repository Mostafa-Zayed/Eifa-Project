     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'introduction' => [
                'en' => $request->introduction['en'],
                'ar' => $request->introduction['ar']
            ],
            'vision' => [
                'en' => $request->vision['en'],
                'ar' => $request->vision['ar']
            ],
            'mission' => [
                'en' => $request->mission['en'],
                'ar' => $request->missing(['ar'])
            ],
            'objectives' => [
                'en' => $request->objectives['en'],
                'ar' => $request->objectives['ar']
            ],
            'status' => $request->status
        ]);

        toastr()->success('Data has been save Successfully');
        return redirect()->route('abouts.index');
    }

